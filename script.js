let currentTrack = 0;
const audio = new Audio(tracks[currentTrack].src);

const trackList = document.getElementById("track-list");
const lyrics = document.getElementById("lyrics");
const playPauseBtn = document.getElementById("play-pause-btn");
const prevBtn = document.getElementById("prev-btn");
const nextBtn = document.getElementById("next-btn");

// Initialiser la liste des pistes
function initTrackList() {
    trackList.innerHTML = "";
    tracks.forEach((track, index) => {
        const trackDiv = document.createElement("div");
        trackDiv.classList.add("track");
        if (index === currentTrack) trackDiv.classList.add("active");
        trackDiv.textContent = track.title;
        trackDiv.addEventListener("click", () => {
            currentTrack = index;
            playTrack();
        });
        trackList.appendChild(trackDiv);
    });
}

// Lecture/Pause
playPauseBtn.addEventListener("click", () => {
    if (audio.paused) {
        audio.play();
        playPauseBtn.textContent = "⏸️";
    } else {
        audio.pause();
        playPauseBtn.textContent = "▶️";
    }
});

// Suivant/Précédent
nextBtn.addEventListener("click", () => {
    currentTrack = (currentTrack + 1) % tracks.length;
    playTrack();
});

prevBtn.addEventListener("click", () => {
    currentTrack = (currentTrack - 1 + tracks.length) % tracks.length;
    playTrack();
});

// Lire une piste
function playTrack() {
    audio.src = tracks[currentTrack].src;
    audio.play();
    playPauseBtn.textContent = "⏸️";
    updateUI();
}

// Mettre à jour l'interface
function updateUI() {
    initTrackList();
    lyrics.textContent = tracks[currentTrack].lyrics || "Aucune parole disponible pour cette chanson.";
}

// Initialisation
initTrackList();
updateUI();